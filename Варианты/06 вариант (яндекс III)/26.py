f = open('26.txt')
n = int(f.readline())
отгрузка = dict()
склад = dict()
for i in range(n):
    артикул, масса, статус = map(int, f.readline().split())
    if артикул  not in склад:
        склад[артикул] = масса
    else:
        склад[артикул] += масса
    if статус == 0:
        if артикул in отгрузка:
            отгрузка[артикул][1] +=1
        else:
            отгрузка[артикул]=[масса,1]



print(отгрузка)